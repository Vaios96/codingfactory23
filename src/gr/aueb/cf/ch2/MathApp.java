package gr.aueb.cf.ch2;

/**
 * Demonstrates the Math class.
 */

public class MathApp {
    public static void main(String[] args) {
        System.out.println("Το απόλυτο του -5 είναι: " + Math.abs(-5));
        System.out.println("Το min μεταξύ 1, 19 είναι: " + Math.min(1, 10));
        System.out.println("Το max μεταξύ 1, 19 είναι: " + Math.max(1, 10));
    }
}
